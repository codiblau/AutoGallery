    public static function getURL() {
        $url = filter_input(INPUT_SERVER, "REQUEST_URI");

        $arrayURL = explode("/", $url);
        $url = end($arrayURL);

        $arrayParams = explode("?", $url);
        $url = $arrayParams[0];

        $url = str_replace(".cb", "", $url);

        $params = str_replace('&', '_ampersand_', (filter_input(INPUT_SERVER, "QUERY_STRING")));


        $obj = new stdClass();
        $obj->url = $url;
        $obj->params = $params;

        return $obj;
