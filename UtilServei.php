    public static function format($valor) {
        if (isset($valor) && $valor != '') {
            return htmlspecialchars($valor, ENT_QUOTES);
        } else {
            return '';
        }
    }

    public static function unFormat($valor) {
        return htmlspecialchars_decode($valor, ENT_QUOTES);
    }

    public static function contains($substring, $string) {
        $pos = strpos($string, $substring);

        if ($pos === false) {
            // string needle NOT found in haystack
            return false;
        } else {
            // string needle found in haystack
            return true;
        }
    }

    public static function formatDate($data, $format = 'd/m/Y H:i:s') {
        try {
            $date = new DateTime($data);
            return $date->format($format);
        } catch (Exception $e) {
            return "";
        }
    }

